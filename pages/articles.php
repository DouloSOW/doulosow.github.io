		<h1>Le Ro au coeur de la transmission
communautaire du Covid-19 au Sénégal</h1>
  <ul><div class="column">
    <div class="card">
      <img src="Images/exp1.png" alt="DIA" style="width:100%">
    </div>
  </div># Ndiaye DIA, école polytechnique de Paris</ul>
<p>L’épidémie du nouveau coronavirus (Covid-19) a provoqué des bouleversements considérables
dans le monde entier. Alors que dans certains pays, on observe un aplatissement de la courbe
des nouveaux cas, d’autres pays sont toujours dans une phase de croissance du nombre de cas
liés au Covid-19. Au Sénégal, un premier cas importé a été détecté en mars 2020. Depuis le
03 mars 2020, le nombre de nouveaux cas par jour suit une croissance non négligeable, allant
jusqu’à l’apparition de clusters (foyer communautaires) à l’intérieur du territoire national.
Cette croissance du nombre de cas confirmés, est conditionnée par différents facteurs dont certains
sont non seulement très difficilement contrôlables, mais aussi peuvent changer d’une ville
à une autre. Parmi ces paramètres, <span class="bleu">le nombre de reproduction de base noté Ro</span>, définissant le nombre moyen
de personnes contaminées par une personne déjà infectée. Maîtriser la valeur de ce paramètre
est primordiale pour comprendre la dynamique de propagation de l’épidémie à l’intérieur du
territoire national.
<br>En fonction des différentes valeurs de Ro, trois cas de figurent permettant de comprendre
l’ampleur de l’épidémie se présentent:<br>Si R0 < 1,
en moyenne une personne infectée contamine moins d’une personne.
Et donc la maladie devrait cesser de se propager. <br>
Si R0 = 1,
une personne infectée contamine en moyenne une personne.
La propagation de la maladie est stable ou endémique, et le nombre d’infections ne devrait ni
augmenter ni diminuer.<br>
Si R0 > 1,
en moyenne une personne infectée contamine plus d’une personne.
Si aucune mesure ferme n’est prise, la maladie devrait se propager de plus en plus. C’est
exactement ce scénario qui est observé dans plusieurs pays actuellement.
<br>
Ainsi, la réduction de la valeur de ce paramètre est d’une importance capitale dans les mesures
non sanitaires proposées pour freiner la propagation du Covid-19 au sein d’une population.
Selon une étude réalisée par the London Imperial College, une méthode efficace pour minimiser le
nombre de reproduction de base, serait guidée par une prise de responsabilité personnelle exercée
par tout un chacun au sein de la population. Ceci, en respectant les mesures barrières qui ont été
instaurées par les autorités, et rester le maximum possible chez-soi. Selon toujours, the Imperial
College of London, en cette période de crise sanitaire, le fait de rester chez-soi permettra de
sauver une vie toutes les huit minutes.À l’aide de méthodes statistiques, avec les données sur le Covid-19 au Sénégal, nous avons estimé
le nombre de reproduction de base (Ro) de l’épidémie au Sénégal, c’est-à-dire le nombre
d’infections secondaires engendrées par une personne infectée au cours de toute sa période infectieuse.
Nous avons réalisé cette estimation en prenant en compte les mesures de restrictions
imposées par les autorités, notamment la fermeture des écoles et université, l’interdiction de
rassemblements regroupant un grand nombre de personnes....
La figure montre les résultats de cette estimation.<br>

