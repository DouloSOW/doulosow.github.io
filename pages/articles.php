<div class="etud" >
		<h1>Impact des mesures d’urgence et de prévention sur la propagation du Covid-19 au Sénégal</h1>
<ul><img class="stat" alt="" src="Images/Diop.jpg"></ul>
<ul><img class="stat" alt="" src="Images/Moussa.jpg" style="float:left;margin-left:0px"></ul>
<p><strong>Résumé</strong><br></p>
<p>Le Sénégal a enregistré depuis le 02 mars 2020, son premier cas positif au coronavirus. Deux mois après l’apparition du premier cas, 
1115 cas positifs ont été enregistrés dont 368 guéris (soit un taux de guérison de 33%), 9 décès (0,81% de létalité) et donc 737 patients 
sous traitement. Depuis les premières semaines de l’épidémie, le gouvernement a pris des mesures d’urgence et de prévention dans sa stratégie 
de lutte contre le covid-19.
Les données utilisées pour modéliser l’évolution du covid-19 au Sénégal, couvrent la période du 02 mars au 02 mai. La méthode utilisée est
 la modélisation par le SIR, modèle de base des études épidémiologiques. Cependant, celui-ci, du fait sa simplicité ne permet pas de prédire 
 le nombre de décès ou de cas graves mais permet de prévoir le pic de l’épidémie et sa situation finale.
</p>
<p>Les résultats de cette présente étude ont montré que dans le contexte actuel de l’évolution de l’épidémie, 
le pic serait atteint dans la première moitié du mois de juillet 2020 et l’épidémie se prolongerait jusque vers la fin du mois de décembre 2020.
 Un renforcement des mesures prises pourrait retarder le pic de l’épidémie au mois de novembre 2020 avec un nombre estimé à 71049 cas positifs, 
 soit 4,7 fois de moins que la première situation. Dans cette configuration la maladie sera complètement éradiquée entre mars et avril 2021 
 avec un total estimé à 1489149 individus infectés durant l’épidémie. En revanche, la stratégie de l’immunité collective qui constitue
  une absence de ces mesures a montré l’ampleur des risques encourus dans ce contexte. En effet, celui-ci aurait précipité l’échéance
   du pic au mois d’avril et l’épidémie prendrait fin au mois de juin 2020. Cependant, un nombre très élevé de cas positifs serait
    enregistré, soit plus de 12 millions personnes contaminées.
Ces conclusions encourageraient l’Etat du Sénégal dans sa stratégie de lutte contre le covid-19 axée sur la mise en œuvre de mesures d’urgence et de prévention. 
</p>
<h3> <strong>Mots clés : Sénégal, Covid-19, Pandémie, SIR.</strong></h3>

