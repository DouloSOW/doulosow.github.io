<ul><img alt="" src="Images/amsa.png"># Amsata NIANG, Statistician consultant at FAO</ul>
<p>La période d’une épidémie peut être décomposée en plusieurs phases. D’abord la phase initiale 
où on note une croissance exponentielle du nombre de personnes infectées par jour (blanc) une 
phase de croissance (respectivement décroissance) rapide des nouveaux cas (rouge) une phase de
 transition (jaune) et la phase finale (vert) (Milan B., 2020). Le nombre cumulé de cas confirmés 
 a ainsi une forme qui peut être approximée par une fonction logistique. D’où l’utilisation du 
 modèle logistique. Le modèle logistique permet d’estimer en particulier la taille finale du 
 nombre de personne infectées. Vers la fin de l’épidémie, la taille finale estimée de façon 
 journalière doit converger. Et cela signifie une stabilité de l’épidémie au niveau du
  pays en question (graphique à droite pour la chine).<br><br></p>

