		<h1>Etude statistique de la COVID-19 : analyse statistique de la situation de la pandémie et prévision économétrique des infectés au Sénégal</h1>
<ul><img class="stat" alt="" src="Images/stat.jpg"># STAT AGENCY </ul>
<p><strong>Résumé</strong><br></p>
<p>Ce papier relate une analyse statistique de la situation de la pandémie de COVID-19 et une prévision économétrique des infectés. La pandémie a démarré à Diourbel en mars
 dernier et s’est poursuivie jusqu’à atteindre là-bas un pic vers la fin du mois de février. A la date du 02 juin 2020, Diourbel constitue 11% du total des cas positifs au Sénégal.
  Ensuite, Thiès a vu augmenter radicalement le nombre de cas infectés depuis le mois de mars, atteignant 5% du total de cas positifs au Sénégal. En même temps, et à des degrés moindres, 
  la région de Dakar a petit à petit pris le relais. Dakar contient aujourd’hui plus de cas positifs que partout ailleurs au Sénégal (74%). Par contre, les régions du Sud à l’instar 
  de Ziguinchor sont devenues l’épicentre de la pandémie à la fin du mois de mai avec 6% des cas positifs au Sénégal. Les régions de l’Est sont modestement touchées par ladite pandémie
   (environ 2% du total des cas positifs), de même que les autres régions du Centre (moins de 20 cas positifs seulement). Les estimations économétriques se sont effectuées en admettant
    que la pandémie de COVID-19 continue jusqu’au mois de décembre. Les résultats montrent que la durée de la pandémie ne dépasserait pas 300 jours au Sénégal, si durant les 10 mois, 
    le nombre de nouveaux cas ne dépasse jamais 200
, et en l’absence de vaccin et traitement efficace, avec un nombre de cas confirmés environ entre 10 000 et 55 000 personnes dont près de 1 000 morts au maximum au 31 décembre 2020.</p>
